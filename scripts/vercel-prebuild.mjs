#!/usr/bin/env node

/**
 * Vercel build-time secrets fetcher.
 *
 * Runs on Vercel's build machine before `next build`. Reads DOPPLER_TOKEN
 * from process.env and fetches all secrets from Doppler's API into a
 * `.env.production.local` file that Next.js consumes during the build.
 *
 * The file lives only on the build sandbox and is discarded when the build
 * VM tears down. Server secrets are NOT shipped to the runtime via this
 * file — at runtime, lib/secrets.ts re-fetches them from Doppler.
 * NEXT_PUBLIC_* values DO get inlined into the client bundle from this file
 * (that's the whole point — Next.js inlines them at build time).
 *
 * Skipped silently if DOPPLER_TOKEN is not set, so legacy-mode builds and
 * developer machines work unchanged.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const ROOT_DIR = path.join(__dirname, '..');
const ENV_PRODUCTION_LOCAL = path.join(ROOT_DIR, '.env.production.local');

const DOPPLER_API_URL = 'https://api.doppler.com/v3/configs/config/secrets/download?format=json';

async function fetchSecretsWithRetry(token, maxAttempts = 3) {
  let lastError;
  for (let attempt = 1; attempt <= maxAttempts; attempt++) {
    try {
      const res = await fetch(DOPPLER_API_URL, {
        method: 'GET',
        headers: { Authorization: `Bearer ${token}`, Accept: 'application/json' },
      });
      if (!res.ok) {
        throw new Error(`Doppler API returned ${res.status} ${res.statusText}`);
      }
      return await res.json();
    } catch (err) {
      lastError = err;
      if (attempt < maxAttempts) {
        const delay = 500 * 2 ** (attempt - 1);
        console.warn(`Doppler fetch failed (attempt ${attempt}/${maxAttempts}): ${err.message}. Retrying in ${delay}ms…`);
        await new Promise((r) => setTimeout(r, delay));
      }
    }
  }
  throw new Error(`Doppler fetch failed after ${maxAttempts} attempts: ${lastError?.message ?? lastError}`);
}

function escapeEnvValue(value) {
  // Wrap any value containing whitespace or special chars in double quotes,
  // escape backslashes and dquotes within. Newlines become literal \n.
  const needsQuotes = /[\s"#$'`\\]/.test(value) || value === '';
  if (!needsQuotes) return value;
  const escaped = String(value)
    .replace(/\\/g, '\\\\')
    .replace(/"/g, '\\"')
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r');
  return `"${escaped}"`;
}

function toEnvFileContent(secrets) {
  const lines = ['# Auto-generated by scripts/vercel-prebuild.mjs from Doppler. Do not commit.'];
  for (const [key, value] of Object.entries(secrets)) {
    if (value === null || value === undefined) continue;
    lines.push(`${key}=${escapeEnvValue(String(value))}`);
  }
  return lines.join('\n') + '\n';
}

async function main() {
  const token = process.env.DOPPLER_TOKEN;
  if (!token) {
    console.log('vercel-prebuild: DOPPLER_TOKEN not set — skipping (legacy mode).');
    return;
  }

  console.log('vercel-prebuild: fetching secrets from Doppler…');
  const secrets = await fetchSecretsWithRetry(token);

  const keyCount = Object.keys(secrets).length;
  if (keyCount === 0) {
    throw new Error('Doppler returned 0 secrets — refusing to build with empty env.');
  }

  fs.writeFileSync(ENV_PRODUCTION_LOCAL, toEnvFileContent(secrets), {
    encoding: 'utf-8',
    mode: 0o600,
  });
  console.log(`vercel-prebuild: wrote ${keyCount} secrets to .env.production.local`);
}

main().catch((err) => {
  console.error(`vercel-prebuild: ${err.message || err}`);
  process.exit(1);
});
